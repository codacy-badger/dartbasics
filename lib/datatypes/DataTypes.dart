class DataTypes {
  int value_1= 10;
  double value_2= 10.10;
  String value_3= 'Wasula Benjamin';
  bool value_4= true;

  DataTypes() {
    print('This is an int value     -> ${value_1}');
    print('This is a double value   -> ${value_2}');
    print('This is an String value  -> ${value_3}');
    print('This is an bool value    -> ${value_4}');
  }
}